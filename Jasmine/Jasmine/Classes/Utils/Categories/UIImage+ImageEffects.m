+ (UIImage *) gradientImageWithSize:(CGSize) size
                          locations:(const CGFloat []) locations
                         components:(const CGFloat []) components
                              count:(NSUInteger)count
{
    UIGraphicsBeginImageContextWithOptions(size, NO, 0);
    CGContextRef context = UIGraphicsGetCurrentContext();
    
    CGColorSpaceRef colorSpace = CGColorSpaceCreateDeviceRGB();
    CGGradientRef colorGradient = CGGradientCreateWithColorComponents(colorSpace, components, locations, count);
    CGColorSpaceRelease(colorSpace);
    CGContextDrawLinearGradient(context, colorGradient, (CGPoint){0, 0}, (CGPoint){size.width, 0}, 0);
    CGGradientRelease(colorGradient);
    
    UIImage *image = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return image;
}

+ (UIImage *)zoomImage:(UIImage *)imageOriginal toSize:(CGSize)sizeMax
{
    if (nil == imageOriginal)
    {
        return nil;
    }
    if (imageOriginal.size.width <= sizeMax.width || imageOriginal.size.height <= sizeMax.height)
    {
        return imageOriginal;
    }
    UIImage *imageZoom = nil;
    
    size_t image_width, image_height;
    //计算拉伸度
    CGFloat fh = sizeMax.width / imageOriginal.size.width;
    CGFloat fv = sizeMax.height / imageOriginal.size.height;
    if (imageOriginal.size.width == imageOriginal.size.height && sizeMax.width == sizeMax.height)
    {
        image_width = sizeMax.width;
        image_height = sizeMax.height;
    }
    else if (fh > fv)
    {
        image_width = imageOriginal.size.width * sizeMax.height / imageOriginal.size.height;
        image_height = sizeMax.height;
    }
    else
    {
        image_width = sizeMax.width;
        image_height = imageOriginal.size.height * sizeMax.width / imageOriginal.size.width;
    }
    
    UIGraphicsBeginImageContext(CGSizeMake(image_width, image_height));
    [imageOriginal drawInRect:CGRectMake(0, 0, image_width, image_height)];
    imageZoom = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return imageZoom;
}

+(UIImage *)scaleImage:(UIImage *)originalImage
{
    if (originalImage == nil) {
        return nil;
    }
    UIImage * scaleImge = nil;
    CGSize imageSize = CGSizeMake(20, 20);
    UIGraphicsBeginImageContext(imageSize);
    [originalImage drawInRect:CGRectMake(0, 0, imageSize.width, imageSize.height)];
    scaleImge = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return scaleImge;
}

